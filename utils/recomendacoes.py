    st.markdown("""
    Com base nas análises de dados sobre sintomas, comportamento da população e impactos econômicos, apresentamos as seguintes recomendações para o hospital em caso de um novo surto de COVID-19:

    ### 1. Foco nas Ações Preventivas Baseadas no Uso de Máscara
    - **Justificativa**: Indivíduos que usaram máscaras regularmente tiveram menos incidência de sintomas em comparação com aqueles que não usaram.
    - **Ação Recomendada**: Implementar o uso obrigatório de máscaras no hospital, oferecendo máscaras gratuitamente para pacientes e visitantes. Além disso, reforçar campanhas educacionais sobre a importância do uso correto das máscaras.

    ### 2. Atenção à Adesão ao Distanciamento Social no Hospital
    - **Justificativa**: O distanciamento social mostrou-se uma medida eficaz na contenção da disseminação da COVID-19.
    - **Ação Recomendada**: Garantir que áreas de espera e triagem sigam rigorosamente as diretrizes de distanciamento social. Implementar sistemas de agendamento para consultas e controle de fluxo de pessoas dentro do hospital.

    ### 3. Ajuste na Capacidade de Atendimento para Diferentes Faixas Etárias
    - **Justificativa**: A faixa etária entre 30 e 59 anos foi a mais afetada pelos sintomas da COVID-19.
    - **Ação Recomendada**: Preparar a infraestrutura do hospital para priorizar o atendimento desse grupo etário, garantindo que eles recebam o tratamento adequado e rápido.

    ### 4. Priorizar o Acesso ao Atendimento por Região
    - **Justificativa**: As regiões menos favorecidas apresentaram uma maior incidência de sintomas graves.
    - **Ação Recomendada**: Melhorar o acesso ao atendimento para pacientes vindos de regiões mais carentes, com postos de triagem ou parcerias com hospitais locais.

    ### 5. Atendimento Especial a Pacientes de Baixa Renda
    - **Justificativa**: Pacientes com renda reduzida foram os mais afetados economicamente e tiveram maior necessidade de atendimento durante a pandemia.
    - **Ação Recomendada**: Garantir acesso a tratamentos subsidiados para pacientes de baixa renda, facilitando o acesso aos cuidados médicos por meio de programas sociais e parcerias com o governo.

    ### Conclusão
    Essas recomendações foram elaboradas com base nos dados analisados e visam preparar o hospital para responder de forma eficaz a novos surtos de COVID-19, focando na proteção das populações mais vulneráveis e na mitigação do impacto econômico e social da pandemia.
    """)